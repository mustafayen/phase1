import unittest
from task_29_code import is_valid_sudoku # Import the function

class TestIsValidSudoku(unittest.TestCase):

    def test_valid_board(self):
        """Test with a valid Sudoku board."""
        valid_board = [
            ["5","3",".",".","7",".",".",".","."],
            ["6",".",".","1","9","5",".",".","."],
            [".","9","8",".",".",".",".","6","."],
            ["8",".",".",".","6",".",".",".","3"],
            ["4",".",".","8",".","3",".",".","1"],
            ["7",".",".",".","2",".",".",".","6"],
            [".","6",".",".",".",".","2","8","."],
            [".",".",".","4","1","9",".",".","5"],
            [".",".",".",".","8",".",".","7","9"]
        ]
        self.assertTrue(is_valid_sudoku(valid_board))

    def test_invalid_row(self):
        """Test with a board containing an invalid row (duplicate number)."""
        invalid_board = [
            ["5","3","3",".","7",".",".",".","."],
            ["6",".",".","1","9","5",".",".","."],
            [".","9","8",".",".",".",".","6","."],
            ["8",".",".",".","6",".",".",".","3"],
            ["4",".",".","8",".","3",".",".","1"],
            ["7",".",".",".","2",".",".",".","6"],
            [".","6",".",".",".",".","2","8","."],
            [".",".",".","4","1","9",".",".","5"],
            [".",".",".",".","8",".",".","7","9"]
        ]
        self.assertFalse(is_valid_sudoku(invalid_board))

    def test_invalid_column(self):
        """Test with a board containing an invalid column (duplicate number)."""
        invalid_board = [
            ["5","3",".",".","7",".",".",".","."],
            ["6",".",".","1","9","5",".",".","."],
            [".","9","8",".",".",".",".","6","."],
            ["8",".",".",".","6",".",".",".","3"],
            ["4",".",".","8",".","3",".",".","1"],
            ["7",".",".",".","2",".",".",".","6"],
            [".","6",".",".",".",".","2","8","."],
            [".",".",".","4","1","9",".",".","5"],
            [".",".",".",".","7",".",".","7","9"]
        ]
        self.assertFalse(is_valid_sudoku(invalid_board))

    def test_invalid_subgrid(self):
        """Test with a board containing an invalid 3x3 subgrid (duplicate number)."""
        invalid_board = [
            ["5","3",".",".","7",".",".",".","."],
            ["6",".",".","1","9","5",".",".","."],
            [".","9","8",".",".",".",".","6","."],
            ["8",".",".",".","6",".",".",".","3"],
            ["4",".",".","8",".","3",".",".","1"],
            ["7",".",".",".","2",".",".",".","6"],
            [".","6",".",".",".",".","2","8","."],
            [".",".",".","4","1","9",".",".","5"],
            [".",".",".","4","8",".",".","7","9"]
        ]
        self.assertFalse(is_valid_sudoku(invalid_board))
