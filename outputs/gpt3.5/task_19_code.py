def sort_numbers(numbers):
    sorted_numbers = sorted(numbers)
    return sorted_numbers

# Example usage
numbers = [5, 2, 8, 1, 9]
sorted_numbers = sort_numbers(numbers)
print(sorted_numbers)  # Output: [1, 2, 5, 8, 9]